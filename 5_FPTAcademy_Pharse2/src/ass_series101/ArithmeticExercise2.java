package ass_series101;

public class ArithmeticExercise2 {
	private static int num1 = 125;
	private static int num2 = 24;

	public static void show() {
		System.out.println("125 + 24 = " + (num1 + num2));
		System.out.println("125 - 24 = " + (num1 - num2));
		System.out.println("125 x 24 = " + (num1 * num2));
		System.out.println("125 / 24 = " + (num1 / num2));
		System.out.println("125 % 24 = " + (num1 % num2));
	}
}
