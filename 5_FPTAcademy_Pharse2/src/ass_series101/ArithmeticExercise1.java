package ass_series101;

public class ArithmeticExercise1 {
	public static void assignOne() {
		int numOne = -5 + 8 * 6;
		int numTwo = (55 + 9) % 9;
		int numThree = 20 + -3 * 5 / 8;
		int numFour = 5 + 15 / 3 * 2 - 8 % 3;

		System.out.println("-5 + 8 * 6 = " + numOne);
		System.out.println("(55 + 9) % 9 = " + numTwo);
		System.out.println("(55 + 9) % 9 = " + numThree);
		System.out.println("(55 + 9) % 9 = " + numFour);
	}
}
